// FUNZIONI

// Funzione per salutare
function saluta() {
    console.log("Ciao!");
}
saluta(); // Stampa "Ciao!"

// Funzione che somma due numeri
function somma(a, b) {
    return a + b;
}
console.log(somma(10, 20)); // Stampa 30

// Funzione che moltiplica due numeri
function moltiplica(a, b) {
    return a * b;
}
console.log(moltiplica(10, 20)); // Stampa 200